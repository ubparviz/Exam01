# Bo'linuvchanlikni tekshirish
# Kirish: 30
# Chiqish: 
# 30 soni 2 ga bo'linadi
# 30 soni 3 ga bo'linadi
# 30 soni 5 ga bo'linadi

number = int(input("Son kirting: "))

if number % 2 == 0:
    print(f"{number} soni 2 ga bo'linadi")
if number % 3 == 0:
    print(f"{number} soni 3 ga bo'linadi")
if number % 5 == 0:
    print(f"{number} soni 5 ga bo'linadi")